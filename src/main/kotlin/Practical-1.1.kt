fun main()
{
    var a:Int = 10;
    var b:Float = 1.2F
    var c:Double = 5454.52
    var d:Long = 525685323
    var e:Char = 'A'
    var f:String = "Ashish"
    var g:Boolean = false
    var h:Short = -23
    var i:Byte = 64

    println("$a is integer")
    println("$b is float")
    println("$c is Double")
    println("$d is long")
    println("$e is character")
    println("$f is string")
    println("$g is boolean")
    println("$h is Short")
    println("$i is Byte")

}
